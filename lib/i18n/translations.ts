/** Look up a translation key like 'hero.title1' */
export function t(lang: Lang, key: string): string {
  const [section, ...rest] = key.split('.')
  const field = rest.join('.')
  return T[lang]?.[section]?.[field] ?? T['en']?.[section]?.[field] ?? key
}
